public class switchCase {
    public static void main(String[] args) {
        int x = 10;

        switch(x){
            case 10:
                System.out.println("X has the value 10");
                break;
            case 11:
                System.out.println("X has the value 11");
                break;
            case 12:
                System.out.println("X has the value 12");
                break;
            case 13:
                System.out.println("X has the value 13");
                break;
        }
    }
}
